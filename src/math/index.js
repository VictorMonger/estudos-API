const sum = (number1, number2) => {
  return number1 + number2;  
}

const subtraction = (number1, number2) => {
  return number1 - number2;  
}

const multiplication = (number1, number2) => {
  return number1 * number2;  
}

const division = (number1, number2) => {
  return number1 / number2;  
}

module.exports = { 
  sum, 
  subtraction, 
  multiplication,
  division, 
}